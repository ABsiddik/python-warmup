print("Here are some of the common built-in types")

print(type("welcome"))        # str (string)
print(type(123))              # int (integer)
print(type(12.5))             # float (floating point number)
print(type(3+4j))             # complex (complex number)
print(type(True))             # bool (boolean)
print(type([1, 2, 3]))        # list
print(type((1, 2, 3)))        # tuple
print(type({1, 2, 3}))        # set
print(type({"a": 1, "b": 2})) # dict
print(type(None))             # NoneType
print(type(b"hello"))         # bytes
print(type(bytearray(5)))     # bytearray
print(type(range(5)))         # range
print(type(frozenset([1,2]))) # frozenset
